first = int(input('Insert number'))
oper = input('Insert logic')
second = int(input('Insert number'))
if oper == '+':
    print(first+second)
elif oper == '-':
    print(first-second)
elif oper == '*':
    print(first*second)
elif oper == '/':
    print(first/second)
elif oper == '**':
    print(first**second)
elif oper == '//':
    print(first//second)
elif oper == '%':
    print(first%second)
